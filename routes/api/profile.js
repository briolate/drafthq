// @route   POST api/profile
// @desc    Create or edit user profile
// @access  Private
router.post(
  '/',
  passport.authenticate('jwt', { session: false }),
  (req, res) => {
    const { errors, isValid } = validateProfileInput(req.body);

    // Check Validation
    if (!isValid) {
      // If any errors, send 400 with errors object
      return res.status(400).json(errors);
    }

    // Get fields
    const profileFields = {};

    profileFields.user = req.user.id;

    if (req.body.handle) {
      profileFields.handle = req.body.handle;
    } else {
      profileFields.handle = '';
    }
    if (typeof req.body.seasons !== 'undefined') {
      profileFields.seasons = req.body.seasons.split(',');
    }
    if (typeof req.body.playoffs !== 'undefined') {
      profileFields.playoffs = req.body.playoffs.split(',');
    }
    if (typeof req.body.championships !== 'undefined') {
      profileFields.championships = req.body.championships.split(',');
    }
    if (typeof req.body.lastplace !== 'undefined') {
      profileFields.lastplace = req.body.lastplace.split(',');
    }

    Profile.findOne({ user: req.user.id }).then(profile => {
      if (profile) {
        //Update
        Profile.findOneAndUpdate(
          { user: req.user.id },
          { $set: profileFields },
          { new: true }
        ).then(profile => res.json(profile));
      } else {
        // Create

        // Check if handle exists
        Profile.findOne({ handle: profileFields.handle }).then(profile => {
          if (profile) {
            errors.handle = 'That profile already exists';
            res.status(400).json(errors);
          }

          // Save Profile
          new Profile(profileFields).save().then(profile => res.json(profile));
        });
      }
    });
  }
);

// @route   Post api/profile/seasons
// @desc    Add season to profile
// @access  Private
router.post(
  '/seasons',
  passport.authenticate('jwt', { session: false }),
  (req, res) => {
    const { errors, isValid } = validateProfileInput(req.body);

    // Check Validation
    if (!isValid) {
      // Return any errors with 400 status
      return res.status(400).json(errors);
    }

    Profile.findOne({ user: req.user.id }).then(profile => {
      const newSeason = {
        year: req.body.year,
        qb: req.body.qb,
        rb1: req.body.rb1,
        rb2: req.body.rb2,
        wr1: req.body.wr1,
        wr2: req.body.wr2,
        te: req.body.te,
        dst: req.body.dst,
        k: req.body.k
      };

      // Add to seasons array
      profile.seasons.unshift(newSeason);

      profile.save().then(profile => res.json(profile));
    });
  }
);

// @route   DELETE api/profile/seasons/:season_id
// @desc    Delete season from profile
// @access  Private
